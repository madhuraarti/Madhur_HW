package Methods;

public class Sample1
{
    public static void main(String[] args)// pre-defined method
    
    {
    	System.out.println("Hi");
    	
    	m1(); //methodname();
    	m2();
    	
    	System.out.println("Hello");
    	
       	
    	
    }
	
   //regular method
    public static void m1() //user defined method ( methodname=m1)
    { 
    	System.out.println("running regular method:m1");
    }
    
    public static void m2() //user defined method ( methodname=m1)
 
    { 
    	System.out.println("running regular method:m2");
    } 
    
}
