package basicsofvariable;

public class demo3 
{

	public static void main(String[] args)
	{
		// 1. Variable declaration ( Allocating/Reserving memory)  // Over come these variable because long statement is not good practice
		
		String sName;    // datatype variableName
		
		String sDeptt;
		
		int sRollNum;
		
		float sPer;
		
		char sGrade;
		
		
		// 2. Variable intialization ( Assigning or inserting value)
		
		sName="Pooja";   // variableName =" variable value";
		
		sDeptt="CS";
		
		sRollNum=101;
		
		sPer =65.5f;
		
		sGrade ='A';
		
		// 3. variable usage
		
		System.out.println(sName);  // information reuse
		
		System.out.println(sDeptt);
		
		System.out.println(sRollNum);
		
		System.out.println(sPer);
		
		System.out.println(sGrade);
		
	}






}
