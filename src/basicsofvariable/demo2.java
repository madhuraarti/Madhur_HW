package basicsofvariable;

public class demo2 
{

	public static void main(String[] args)
	{
		// 1. Variable declaration ( Allocating/Reserving memory)
		
		String studentName;    // datatype variableName
		
		String studentDeptt;
		
		int studentRollNum;
		
		float studentPer;
		
		char studentGrade;
		
		
		// 2. Variable intialization ( Assigning or inserting value)
		
		studentName="Gopal";   // variableName =" variable value";
		
		studentDeptt="Mechanical";
		
		studentRollNum=101;
		
		studentPer =75.5f;
		
		studentGrade ='D';
		
		// 3. variable usage
		
		System.out.println(studentName);  // information reuse
		
		System.out.println(studentDeptt);
		
		System.out.println(studentRollNum);
		
		System.out.println(studentPer);
		
		System.out.println(studentGrade);
		
	}



}
