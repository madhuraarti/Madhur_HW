package Conditional_Statement;

public class Example2_1_if_else 

{

	public static void main(String[] args)
	
	{
			int marks=34;
			
			if(marks>=35) // condition --> true
			{
			System.out.println("Pass");	
		    }
			
			else // condition---> false
			{
				System.out.println("Fail");	
			}
		
	}




}
