package Conditional_Statement;

public class Example5_2_switch
{

	public static void main(String[] args)
	{
		String inp="CW";
		switch(inp)
		{
		case "CD": System.out.println("Mini Statement 1");
		break;
		case "CW": System.out.println("Mini Statement 2");
		break;
		case "BI": System.out.println("Mini Statement 3");
		break;
		case "BA": System.out.println("Mini Statement 4");
		break;
				
		default: System.out.println("wrong input");
		}

	}





}
