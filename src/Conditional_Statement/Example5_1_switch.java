package Conditional_Statement;

public class Example5_1_switch
{

	public static void main(String[] args)
	{
		switch("CD")
		{
		case "CD": System.out.println("Mini Statement1");
		break;
		case "CW": System.out.println("Mini Statement2");
		break;
		case "BI": System.out.println("Mini Statement3");
		break;
		case "BA": System.out.println("Mini Statement4");
		break;
				
		default: System.out.println("wrong input");
		}

	}
}
