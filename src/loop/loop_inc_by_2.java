package loop;

public class loop_inc_by_2
{
	public static void main(String[] args)
	{ 
		    //print num from 2 to 9 even number
		  
		//start num:2
	    		//end num:9
	     //increment or decrement: increment by 1
	
		   

		for(int i=2;   i<=9  ; i=i+2)
		{
	       System.out.println(i);    
		
		}
		
		
		
		for(int i=11;   i<=19  ; i=i+2)
		{
	       System.out.println(i);    
		
		}
	
	}

}
